
public class SwitchMain02 {
	public static void main(String[] args) {
		char a = '가';
		
		switch(a) {
			case '가':
				System.out.println("가 입력");
				break;
			case '나':
				System.out.println("나 입력");
				break;
			case '다':
				System.out.println("다 입력");
				break;
			case '라' :
				System.out.println("라 입력");
				break;
				
		}
	}
}
