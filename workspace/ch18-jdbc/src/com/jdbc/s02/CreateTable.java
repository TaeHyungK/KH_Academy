package com.jdbc.s02;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.SQLException;
import java.sql.Statement;

public class CreateTable {
	public static void main(String[] args) {
		String driverName = "oracle.jdbc.OracleDriver";
		String dbUrl = "jdbc:oracle:thin:@localhost:1521:xe";
		String id = "scott";
		String password = "tiger";
		
		Connection conn = null;
		Statement stmt = null;
		
		try {
			//JDBC 수행 1단계 : 드라이버 로드
			Class.forName(driverName);
			//JDBC 수행 2단계 : Connection 객체 생성(ID,비번 인증)
			conn = DriverManager.getConnection(dbUrl, id, password);
			
			System.out.println("test1 테이블을 생성합니다.");
			
			//JDBC 수행 3단계 : Statement 객체 생성 -> SQL문장을 실어 나르는 역할
			stmt = conn.createStatement();
			
			//테이블을 생성하는 SQL문
			//접속한 계정에 테이블명이 중복되면 에러가 발생하기 때문에
			//동일 계정에서는 한 번만 수행함.
			String sql = "CREATE TABLE test1 (id varchar2(10), age number)";
			
			//JDBC 수행 4단계 : SQL문을 실행해 DB에 테이블을 반영
			stmt.executeUpdate(sql);
			
			System.out.println("테이블이 정상적으로 생성되었습니다.");
			
			
		}catch(SQLException e) {
			e.printStackTrace();
		}catch(ClassNotFoundException e){
			e.printStackTrace();
		}finally {
			//자원 정리
			if(stmt != null) try {stmt.close();} catch(Exception e) {}
			if(conn != null) try {conn.close();} catch(Exception e) {}
		}
	}
}
